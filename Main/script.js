// Conversion 

const year = "2000";
console.log(year + 18); // 200018
console.log(Number(year) + 18); // 2018

// coercion

console.log("18" + "10" + 9); // 18109
console.log("18" - "10" - 9); // -1 

// more examples

let n = "1" - 1;
console.log(n); // 0

n = n + "10"; 
console.log(n); // 010



