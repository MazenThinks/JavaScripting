console.log("--- Let ---");

// let - value can be changed 
let lett = 31;
console.log("1st Value ? " + lett)

lett = 32;
console.log("2nd value ? " + lett);

// ---
console.log("--- Var ---");
//---

// Var - Same as Let 
var varr = 31;
console.log("1st Value ? " + varr);

varr = 32;
console.log("2nd Value ? " + varr);

//---
console.log("--- Const ---");
//---

// Const - unchangeable value 
const constt = 31;
console.log("1st Value ? " + constt);

